/**
 * Condensed font converter: reads rom/assets/font/condensed.png (GBStudio format)
 * and generates a GB 2bpp tile data C source file.
 *
 * Font PNG format (GBStudio):
 *   - 128×112 pixels, 4-bit indexed palette, magenta (#FF00FF) = background/transparent
 *   - 16 columns × 14 rows of 8×8 pixel character tiles
 *   - Tile index N = ASCII character code N
 *   - Tiles 0-31: control chars (skipped — blank)
 *   - Tiles 32-223: printable chars (loaded into VRAM, tile 0 = space = ASCII 32)
 *
 * Output: font_data.c — const uint8_t font_data[] in GB 2bpp format.
 * startup.s references _font_data by linker symbol and bulk-copies it into VRAM.
 * Tile index in VRAM = ASCII code - 0x20 (same mapping as the old hand-coded font).
 */

import { readFile, writeFile, mkdir } from "fs/promises";
import { join } from "path";
import { parsePng } from "./png.js";
import type { ParsedPng } from "./png.js";
import { extractTilesFromSheet } from "./tiles.js";

// Font sheet geometry (condensed.png is always 128×112)
const FONT_PNG_W = 128;
const FONT_PNG_H = 112;
const TILE_W = 8;
const TILE_H = 8;
const COLS = FONT_PNG_W / TILE_W;   // 16
const ROWS = FONT_PNG_H / TILE_H;   // 14
const TOTAL_TILES = COLS * ROWS;     // 224

// Only load printable ASCII (0x20-0x7E = 95 chars).
// PNG tile 0 = space (ASCII 32); tile N = ASCII (N+32); VRAM tile N = tile N.
// Tiles 95-223 (above 0x7E) are never drawn, so skip them to free VRAM for
// scratch tiles used by the proportional blitter.
const FIRST_TILE = 0;
const TILE_COUNT = 95; // ASCII 0x20..0x7E

export interface FontInfo {
  /** Number of font tiles loaded into VRAM (95). */
  tileCount: number;
}

export interface FontResult extends FontInfo {
  /** Raw GB 2bpp tile data (1520 bytes). */
  tileData: Uint8Array;
  /** Per-character advance widths (95 bytes). */
  widthData: Uint8Array;
}

export async function generateFont(
  assetDir: string,
  outputDir: string
): Promise<FontResult> {
  await mkdir(outputDir, { recursive: true });

  const pngBuf = await readFile(join(assetDir, "font", "condensed.png"));
  const png = parsePng(pngBuf, "condensed.png");

  if (png.width !== FONT_PNG_W || png.height !== FONT_PNG_H) {
    throw new Error(
      `condensed.png: expected ${FONT_PNG_W}×${FONT_PNG_H} pixels, ` +
        `got ${png.width}×${png.height}`
    );
  }

  const tileData = extractGbTiles(png);
  const widths = computeCharWidths(png);
  await writeFile(join(outputDir, "font_data.c"), buildCSource(tileData, widths), "utf8");
  await writeFile(join(outputDir, "font_data.h"), buildHeaderSource(), "utf8");

  return { tileCount: TILE_COUNT, tileData, widthData: widths };
}

// ── GB 2bpp tile extractor ───────────────────────────────────────────────────

function extractGbTiles(png: ParsedPng, count: number = TILE_COUNT): Uint8Array {
  const cols = Math.floor(png.width / TILE_W);
  return extractTilesFromSheet(png, FIRST_TILE, count, cols);
}

/**
 * Compute per-character advance widths in pixels.
 * For each tile, scan columns right-to-left to find the rightmost non-background pixel.
 * Advance width = rightmost_col + 2 (1 for the pixel itself + 1px inter-character gap).
 * Blank tiles (space etc.) use a fixed width of 4.
 */
function computeCharWidths(png: ParsedPng): Uint8Array {
  const { width: pngWidth, shades } = png;
  const cols = Math.floor(pngWidth / TILE_W);
  const widths = new Uint8Array(TILE_COUNT);

  for (let t = 0; t < TILE_COUNT; t++) {
    const absIdx = FIRST_TILE + t;
    const tileRow = Math.floor(absIdx / cols);
    const tileCol = absIdx % cols;
    const baseX = tileCol * TILE_W;
    const baseY = tileRow * TILE_H;

    let rightmost = -1;
    for (let py = 0; py < TILE_H; py++) {
      for (let px = 0; px < TILE_W; px++) {
        if (shades[(baseY + py) * pngWidth + (baseX + px)] !== 0 && px > rightmost) {
          rightmost = px;
        }
      }
    }

    // Blank tile (space) → width 4; others → rightmost ink column + 2
    widths[t] = rightmost >= 0 ? rightmost + 2 : 4;
  }

  return widths;
}

// ── C source generator ───────────────────────────────────────────────────────

function buildCSource(tileData: Uint8Array, widths: Uint8Array): string {
  const numTiles = tileData.length / 16;

  // Format as one tile (16 bytes) per line for readability.
  const tileLines: string[] = [];
  for (let t = 0; t < numTiles; t++) {
    const chunk = Array.from(tileData.slice(t * 16, t * 16 + 16))
      .map((b) => `0x${b.toString(16).padStart(2, "0").toUpperCase()}U`)
      .join(", ");
    tileLines.push(`    /* tile ${FIRST_TILE + t} */ ${chunk}`);
  }

  const widthBytes = Array.from(widths)
    .map((w) => `${w}U`)
    .join(", ");

  return [
    "/* Auto-generated by the gbs-master build tool. DO NOT EDIT. */",
    "#include <stdint.h>",
    "",
    `/* Condensed font: ${numTiles} tiles (ASCII 0x20-0x7E) in GB 2bpp format.`,
    ` * Tile 0 = space (ASCII 0x20); tile N = ASCII (N + 0x20). Total: ${tileData.length} bytes.`,
    ` * BGP 0xE4 palette: shade0=white, shade1=lgray, shade2=dgray, shade3=black. */`,
    `const uint8_t font_data[${tileData.length}U] = {`,
    tileLines.join(",\n"),
    "};",
    "",
    `/* Per-character advance widths in pixels (includes 1-pixel inter-character gap).`,
    ` * Computed from condensed.png: rightmost non-background column index + 2. */`,
    `const uint8_t font_widths[${widths.length}U] = {`,
    `    ${widthBytes}`,
    "};",
    "",
  ].join("\n");
}

function buildHeaderSource(): string {
  return [
    "/* Auto-generated by the gbs-master build tool. DO NOT EDIT. */",
    "#ifndef FONT_DATA_H",
    "#define FONT_DATA_H",
    "#include <stdint.h>",
    `extern const uint8_t font_data[${TILE_COUNT * 16}U];`,
    `extern const uint8_t font_widths[${TILE_COUNT}U];`,
    "#endif /* FONT_DATA_H */",
    "",
  ].join("\n");
}

// ── Soft font (fixed-width, used for track list) ─────────────────────────────

/**
 * Generate soft_font_data.c and soft_font_data.h from soft.png.
 * Same PNG format as condensed.png (128×112, 4-bit indexed, GBStudio Classic palette).
 * No per-character widths — the soft font is fixed-width (full 8px per tile).
 * Tiles are loaded into VRAM starting at tile 95 (immediately after the condensed font).
 */
export interface SoftFontResult {
  /** Raw GB 2bpp tile data (1632 bytes). */
  tileData: Uint8Array;
}

/** Number of soft font tiles: ASCII 0x20-0x85 (102 tiles, includes ellipsis). */
const SOFT_TILE_COUNT = 0x85 - 0x20 + 1; // 102
const SOFT_TILE_BYTES = SOFT_TILE_COUNT * 16; // 1632

export async function generateSoftFont(
  assetDir: string,
  outputDir: string
): Promise<SoftFontResult> {
  await mkdir(outputDir, { recursive: true });

  // Load pre-processed 2bpp tile data (the source PNG cannot be distributed).
  const tileData = new Uint8Array(await readFile(join(assetDir, "font", "soft.2bpp")));

  if (tileData.length !== SOFT_TILE_BYTES) {
    throw new Error(
      `soft.2bpp: expected ${SOFT_TILE_BYTES} bytes, got ${tileData.length}`
    );
  }

  await writeFile(join(outputDir, "soft_font_data.c"), buildSoftCSource(tileData), "utf8");
  await writeFile(join(outputDir, "soft_font_data.h"), buildSoftHeaderSource(SOFT_TILE_COUNT), "utf8");

  return { tileData };
}

function buildSoftCSource(tileData: Uint8Array): string {
  const numTiles = tileData.length / 16;
  const tileLines: string[] = [];
  for (let t = 0; t < numTiles; t++) {
    const chunk = Array.from(tileData.slice(t * 16, t * 16 + 16))
      .map((b) => `0x${b.toString(16).padStart(2, "0").toUpperCase()}U`)
      .join(", ");
    tileLines.push(`    /* tile ${FIRST_TILE + t} */ ${chunk}`);
  }
  return [
    "/* Auto-generated by the gbs-master build tool. DO NOT EDIT. */",
    "#include <stdint.h>",
    "",
    `/* Soft font: ${numTiles} fixed-width tiles (ASCII 0x20-0x7E) in GB 2bpp format.`,
    ` * Tile 0 = space (ASCII 0x20); tile N = ASCII (N + 0x20). Total: ${tileData.length} bytes.`,
    ` * Loaded into VRAM at tile 95 (after condensed font). Used for direct-tile track list. */`,
    `const uint8_t soft_font_data[${tileData.length}U] = {`,
    tileLines.join(",\n"),
    "};",
    "",
  ].join("\n");
}

function buildSoftHeaderSource(tileCount: number = TILE_COUNT): string {
  return [
    "/* Auto-generated by the gbs-master build tool. DO NOT EDIT. */",
    "#ifndef SOFT_FONT_DATA_H",
    "#define SOFT_FONT_DATA_H",
    "#include <stdint.h>",
    `extern const uint8_t soft_font_data[${tileCount * 16}U];`,
    "#endif /* SOFT_FONT_DATA_H */",
    "",
  ].join("\n");
}
