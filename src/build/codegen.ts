/**
 * Code generation: produces the assembly config table and trampoline files
 * that bridge the TypeScript build tool and the C player ROM.
 *
 * Two assembly files are written to rom/src/generated/:
 *
 *   config.s       — 128-byte config table at ROM address 0x0280 with
 *                    per-GBS values (addresses, track count, metadata)
 *   trampolines.s  — Entry point + GBS INIT/PLAY call wrappers
 *
 * The config table approach replaces compile-time #defines with runtime
 * reads from a fixed ROM address, enabling template-based ROM building
 * where the web app patches these bytes directly.
 */

import { mkdir, writeFile } from "fs/promises";
import { join } from "path";
import type { ParsedGbs } from "../gbs/types.js";
import type { Track } from "../playlist/types.js";
import type { WramLayout } from "./wram.js";

// Re-export buildTrackData from the pure module (browser-safe).
export { buildTrackData } from "./track-data.js";

// Maximum lengths for config table string fields.
const MAX_TITLE_LEN = 30;
const MAX_AUTHOR_LEN = 30;

/** Options for banked-code mode (player _CODE in a dedicated ROM bank). */
export interface BankedCodeOptions {
  /** True when _CODE must live in a separate ROM bank to avoid GBS overlap. */
  enabled: boolean;
  /** ROM bank number where _CODE is placed (only meaningful when enabled). */
  codeBankNum: number;
}

/**
 * Generate config.s — the per-GBS configuration table at ROM 0x0280.
 * Contains GBS addresses, track count, resource bank, stack pointers,
 * and album metadata as binary data.
 */
export async function generateConfig(
  gbs: ParsedGbs,
  tracks: Track[],
  outputDir: string,
  overrides?: { title?: string; wram?: WramLayout; resourceBank?: number },
): Promise<string> {
  await mkdir(outputDir, { recursive: true });

  const path = join(outputDir, "config.s");
  const { header } = gbs;

  const stackAddr = overrides?.wram?.stackAddr ?? 0xC300;
  const resBank = overrides?.resourceBank ?? 2;

  const title = toAscii(overrides?.title ?? header.title);
  const author = toAscii(header.author);
  const copyright = toAscii(header.copyright);

  const lines = [
    "; Auto-generated by the gbs-master build tool. DO NOT EDIT.",
    "; Per-GBS configuration table at ROM address 0x0280.",
    "; See config.h for field offsets and accessor macros.",
    "",
    ".area _CONFIG (ABS)",
    "",
    ".org 0x0280",
    "_config_table::",
    `    .dw #${hex16(header.initAddr)}  ; +0x00: gbs_init_addr`,
    `    .dw #${hex16(header.playAddr)}  ; +0x02: gbs_play_addr`,
    `    .dw #${hex16(header.stackPtr)}  ; +0x04: gbs_stack_ptr`,
    `    .db #${hex8(header.timerModulo)}          ; +0x06: timer_modulo`,
    `    .db #${hex8(header.timerControl)}          ; +0x07: timer_control`,
    `    .db #${gbs.usesTimerInterrupt ? "0x01" : "0x00"}          ; +0x08: use_timer`,
    `    .db #${tracks.length}           ; +0x09: num_tracks`,
    `    .db #${resBank}           ; +0x0A: resource_bank`,
    `    .dw #${hex16(stackAddr)} ; +0x0B: player_stack_ptr`,
    `    ; +0x0D: album_title (31 bytes, null-terminated)`,
    ...asmStringField(title, MAX_TITLE_LEN),
    `    ; +0x2C: album_author (31 bytes, null-terminated)`,
    ...asmStringField(author, MAX_AUTHOR_LEN),
    `    ; +0x4B: album_copyright (31 bytes, null-terminated)`,
    ...asmStringField(copyright, MAX_TITLE_LEN),
    `    ; Pad to 128 bytes total (0x0280 + 0x80 = 0x0300)`,
    `    .ds ${128 - 0x6A}`,
    "",
  ];

  const content = lines.join("\n");
  await writeFile(path, content, "utf8");
  return path;
}

/**
 * Generate `trampolines.s` — a small ASM file that provides the `_entry`
 * symbol called by startup.s, plus `_gbs_init_trampoline` and
 * `_gbs_play_trampoline` used by player.c.
 *
 * In non-banked mode: simple call-through trampolines.
 * In banked mode:     trampolines handle MBC bank switching around GBS calls
 *                     and provide a generic banked-copy routine.
 */
export async function generateTrampolines(
  outputDir: string,
  bankedCode: BankedCodeOptions,
  gbsInitAddr: number,
  gbsPlayAddr: number,
): Promise<string> {
  const path = join(outputDir, "trampolines.s");
  const content = bankedCode.enabled
    ? buildBankedTrampolines(bankedCode.codeBankNum, gbsInitAddr, gbsPlayAddr)
    : buildSimpleTrampolines(gbsInitAddr, gbsPlayAddr);
  await writeFile(path, content, "utf8");
  return path;
}

// ── Trampoline builders ───────────────────────────────────────────────────────

function buildSimpleTrampolines(gbsInitAddr: number, gbsPlayAddr: number): string {
  const initHex = hex16(gbsInitAddr);
  const playHex = hex16(gbsPlayAddr);

  return [
    "; Auto-generated by the gbs-master build tool. DO NOT EDIT.",
    "; Non-banked mode: _entry jumps to _main.",
    "; Trampolines provide uniform call targets for player.c.",
    "",
    ".area _TRAMPOLINE (ABS)",
    ".globl _main",
    "",
    ".org 0x0200",
    "_entry::",
    "    jp   _main",
    "",
    "; ── GBS INIT trampoline ──────────────────────────────────────────────",
    "; Called from player.c with A = 0-based track index, SP = GBS stack.",
    `_gbs_init_trampoline::                  ; ROM offset 0x0203`,
    `    call ${initHex}                     ; CD xx xx — patchable at 0x0204-0x0205`,
    "    ret",
    "",
    "; ── GBS PLAY trampoline ──────────────────────────────────────────────",
    "; Called from player.c with SP = GBS stack.",
    `_gbs_play_trampoline::                  ; ROM offset 0x0207`,
    `    call ${playHex}                     ; CD xx xx — patchable at 0x0208-0x0209`,
    "    ret",
    "",
  ].join("\n");
}

function buildBankedTrampolines(
  codeBankNum: number,
  gbsInitAddr: number,
  gbsPlayAddr: number,
): string {
  const initHex = hex16(gbsInitAddr);
  const playHex = hex16(gbsPlayAddr);

  return [
    "; Auto-generated by the gbs-master build tool. DO NOT EDIT.",
    "; Banked-code mode: player _CODE lives in a dedicated ROM bank.",
    "; These trampolines live in bank 0 (always visible) and handle",
    "; bank switching around GBS calls and resource bank data copies.",
    "",
    ".area _TRAMPOLINE (ABS)",
    ".globl _main",
    ".globl _bcopy_params",
    "",
    ".org 0x0200",
    "",
    "; ── Entry: switch to code bank and call main (never returns) ─────────",
    "_entry::",
    `    ld   a, #${codeBankNum}`,
    "    ld   (0x2000), a",
    "    call _main",
    "_entry_halt:",
    "    halt",
    "    jr   _entry_halt",
    "",
    "; ── GBS INIT trampoline ──────────────────────────────────────────────",
    "; Called from code bank with A = 0-based track index, SP = GBS stack.",
    "; Caller has already saved our SP to WRAM.",
    "; Selects bank 1 before calling GBS INIT (drivers expect their data",
    "; in the switchable window), then restores the code bank on return.",
    "_gbs_init_trampoline::",
    "    push af",
    "    ld   a, #1",
    "    ld   (0x2000), a",
    "    pop  af",
    `    call ${initHex}`,
    `    ld   a, #${codeBankNum}`,
    "    ld   (0x2000), a",
    "    ret",
    "",
    "; ── GBS PLAY trampoline ──────────────────────────────────────────────",
    "; Called from code bank with SP = GBS stack.",
    "; Caller has already saved our SP and player_current_track to WRAM.",
    "; Same bank 1 selection as INIT trampoline.",
    "_gbs_play_trampoline::",
    "    ld   a, #1",
    "    ld   (0x2000), a",
    `    call ${playHex}`,
    `    ld   a, #${codeBankNum}`,
    "    ld   (0x2000), a",
    "    ret",
    "",
    "; ── Generic banked copy ──────────────────────────────────────────────",
    "; Reads BankedCopyParams from WRAM (_bcopy_params):",
    ";   +0: bank (uint8)   +1: src (uint16 LE)   +3: dst (uint16 LE)   +5: len (uint16 LE)",
    "; Switches to the source bank, copies len bytes from src to dst,",
    "; then restores the code bank and returns.",
    "_banked_copy::",
    "    ; Switch to source bank",
    "    ld   a, (_bcopy_params + 0)",
    "    ld   (0x2000), a",
    "    ; Load src into HL",
    "    ld   a, (_bcopy_params + 1)",
    "    ld   l, a",
    "    ld   a, (_bcopy_params + 2)",
    "    ld   h, a",
    "    ; Load dst into DE",
    "    ld   a, (_bcopy_params + 3)",
    "    ld   e, a",
    "    ld   a, (_bcopy_params + 4)",
    "    ld   d, a",
    "    ; Load len into BC",
    "    ld   a, (_bcopy_params + 5)",
    "    ld   c, a",
    "    ld   a, (_bcopy_params + 6)",
    "    ld   b, a",
    "    ; Check for zero length",
    "    ld   a, b",
    "    or   c",
    "    jr   z, _bc_done",
    "_bc_loop:",
    "    ld   a, (hl+)",
    "    ld   (de), a",
    "    inc  de",
    "    dec  bc",
    "    ld   a, b",
    "    or   c",
    "    jr   nz, _bc_loop",
    "_bc_done:",
    `    ld   a, #${codeBankNum}`,
    "    ld   (0x2000), a",
    "    ret",
    "",
    "; ── BankedCopyParams in WRAM ─────────────────────────────────────────",
    ".area _DATA",
    "_bcopy_params::",
    "    .ds 7",
    "",
  ].join("\n");
}

// ── Utilities ─────────────────────────────────────────────────────────────────

function hex16(n: number): string {
  return "0x" + n.toString(16).toUpperCase().padStart(4, "0");
}

function hex8(n: number): string {
  return "0x" + n.toString(16).toUpperCase().padStart(2, "0");
}

/**
 * Replace non-printable-ASCII characters with "?".
 * Our font only covers ASCII 0x20-0x7E.
 */
function toAscii(s: string): string {
  return s.replace(/[^\x20-\x7e]/g, "?");
}

/** Escape a string for safe embedding in sdasgb .ascii directive. */
function escapeAsm(s: string): string {
  return s
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"');
}

/**
 * Emit a fixed-size null-terminated string field as sdasgb directives.
 * Produces: .ascii "text" / .db 0x00 / .ds <padding>
 * Total size is always fieldSize (= maxLen + 1 for the null terminator).
 */
function asmStringField(s: string, maxLen: number): string[] {
  const truncated = s.length > maxLen ? s.slice(0, maxLen) : s;
  const escaped = escapeAsm(truncated);
  const padding = maxLen - truncated.length; // bytes after null terminator
  const lines: string[] = [];
  if (truncated.length > 0) {
    lines.push(`    .ascii "${escaped}"`);
  }
  lines.push("    .db 0x00");
  if (padding > 0) {
    lines.push(`    .ds ${padding}`);
  }
  return lines;
}

// ── Legacy API (kept for backward compatibility during transition) ─────────

/**
 * @deprecated Use generateConfig() + generateTrampolines() instead.
 * Kept temporarily so existing tests don't break.
 */
export async function generateCode(
  gbs: ParsedGbs,
  tracks: Track[],
  outputDir: string,
  overrides?: { title?: string; wram?: WramLayout; bankedCode?: BankedCodeOptions }
): Promise<{ headerPath: string; sourcePath: string }> {
  await mkdir(outputDir, { recursive: true });
  // Generate config.s instead of playlist_data.h/c
  const configPath = await generateConfig(gbs, tracks, outputDir, {
    title: overrides?.title,
    wram: overrides?.wram,
  });
  return { headerPath: configPath, sourcePath: configPath };
}
