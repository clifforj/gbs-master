/**
 * Code generation: produces the C header and source file that bridge the
 * TypeScript build tool and the C player ROM.
 *
 * Two files are written to rom/src/generated/:
 *
 *   playlist_data.h  — #defines for GBS addresses + extern declarations
 *   playlist_data.c  — const arrays with per-track titles
 *
 * Both files are #gitignored (they are always re-generated before compilation)
 * and are what make the player ROM "know" about a specific GBS file at
 * compile time, without needing binary patching after compilation.
 */

import { mkdir, writeFile } from "fs/promises";
import { join } from "path";
import type { ParsedGbs } from "../gbs/types.js";
import type { Track } from "../playlist/types.js";
import type { WramLayout } from "./wram.js";

// Maximum lengths enforced by the GB player UI layout (in characters).
// The condensed font averages ~4 px/char on a 160 px screen, so ~40 chars can
// fit in the worst case.  blit_string() clips at the pixel boundary anyway.
const MAX_TITLE_LEN = 30;
const MAX_AUTHOR_LEN = 30;
const MAX_TRACK_TITLE_LEN = 30;

/** Options for banked-code mode (player _CODE in a dedicated ROM bank). */
export interface BankedCodeOptions {
  /** True when _CODE must live in a separate ROM bank to avoid GBS overlap. */
  enabled: boolean;
  /** ROM bank number where _CODE is placed (only meaningful when enabled). */
  codeBankNum: number;
}

/**
 * Generate playlist_data.h and playlist_data.c into `outputDir`, then return
 * the paths of the written files.
 */
export async function generateCode(
  gbs: ParsedGbs,
  tracks: Track[],
  outputDir: string,
  overrides?: { title?: string; wram?: WramLayout; bankedCode?: BankedCodeOptions }
): Promise<{ headerPath: string; sourcePath: string }> {
  await mkdir(outputDir, { recursive: true });

  const headerPath = join(outputDir, "playlist_data.h");
  const sourcePath = join(outputDir, "playlist_data.c");

  await writeFile(headerPath, buildHeader(gbs, tracks, overrides), "utf8");
  await writeFile(sourcePath, buildSource(gbs, tracks), "utf8");

  return { headerPath, sourcePath };
}

// ── Header file ───────────────────────────────────────────────────────────────

function buildHeader(gbs: ParsedGbs, tracks: Track[], overrides?: { title?: string; wram?: WramLayout; bankedCode?: BankedCodeOptions }): string {
  const { header } = gbs;

  const lines: string[] = [
    "/* Auto-generated by the gbs-master build tool. DO NOT EDIT. */",
    "#ifndef PLAYLIST_DATA_H",
    "#define PLAYLIST_DATA_H",
    "",
    "#include <stdint.h>",
    "",
    "/* ── GBS playback addresses ─────────────────────────────────────────────── */",
    "/* These are Game Boy memory addresses baked into the GBS file header.       */",
    "/* The player ROM calls INIT once per track change and PLAY once per frame.  */",
    "",
    `#define GBS_INIT_ADDR   0x${hex16(header.initAddr)}  /* call with 0-based track index in register A */`,
    `#define GBS_PLAY_ADDR   0x${hex16(header.playAddr)}  /* call every VBlank (or timer) interrupt       */`,
    `#define GBS_STACK_PTR   0x${hex16(header.stackPtr)}  /* initial SP value expected by the GBS code   */`,
    "",
    "/* ── Timer / interrupt configuration ───────────────────────────────────── */",
    `#define GBS_TIMER_MODULO   0x${hex8(header.timerModulo)}  /* loaded into TMA register */`,
    `#define GBS_TIMER_CONTROL  0x${hex8(header.timerControl)}  /* loaded into TAC register */`,
    `#define GBS_USE_TIMER      ${gbs.usesTimerInterrupt ? "1" : "0"}  /* 1 = timer interrupt, 0 = VBlank */`,
    "",
    "/* ── Banked code mode ────────────────────────────────────────────────────── */",
    `#define BANKED_CODE        ${overrides?.bankedCode?.enabled ? "1" : "0"}  /* 1 = _CODE in dedicated bank */`,
    ...(overrides?.bankedCode?.enabled ? [
      `#define CODE_BANK          ${overrides.bankedCode.codeBankNum}  /* ROM bank for player _CODE */`,
    ] : []),
    "",
    "/* ── Player WRAM layout (auto-placed to avoid GBS driver conflicts) ────── */",
    `#define PLAYER_STACK_PTR   0x${hex16(overrides?.wram?.stackAddr ?? 0xC300)}  /* player SP (grows down) */`,
    "",
    "/* ── Track list ─────────────────────────────────────────────────────────── */",
    `#define GBS_NUM_TRACKS   ${tracks.length}U  /* playlist track count */`,
    `#define GBS_FIRST_TRACK  1U  /* playlist index (always 1) */`,
    "",
    "/* ── Album metadata (from GBS header) ───────────────────────────────────── */",
    `#define ALBUM_TITLE     "${truncate(toAscii(overrides?.title ?? header.title), MAX_TITLE_LEN)}"`,
    `#define ALBUM_AUTHOR    "${truncate(toAscii(header.author), MAX_AUTHOR_LEN)}"`,
    `#define ALBUM_COPYRIGHT "${truncate(toAscii(header.copyright), MAX_TITLE_LEN)}"`,
    "",
    "/* Per-track data stored in ROM. */",
    "typedef struct {",
    "    const char *title;",
    "    uint8_t     gbs_track;      /* 1-based GBS track number passed to INIT */",
    "} TrackInfo;",
    "",
    "extern const TrackInfo TRACK_LIST[GBS_NUM_TRACKS];",
    "",
    "#if BANKED_CODE",
    "/* Banked copy: parameter block filled by caller, read by bank-0 _banked_copy. */",
    "typedef struct {",
    "    uint8_t        bank;",
    "    const uint8_t *src;",
    "    uint8_t       *dst;",
    "    uint16_t       len;",
    "} BankedCopyParams;",
    "extern BankedCopyParams bcopy_params;",
    "extern void banked_copy(void);",
    "#endif",
    "",
    "#endif /* PLAYLIST_DATA_H */",
    "",
  ];

  return lines.join("\n");
}

// ── Source file ───────────────────────────────────────────────────────────────

function buildSource(gbs: ParsedGbs, tracks: Track[]): string {
  const { header } = gbs;

  const entries = tracks.map((t) => {
    const title = truncate(escapeC(t.title), MAX_TRACK_TITLE_LEN);
    return `    { "${title}", ${t.number}U }`;
  });

  const lines: string[] = [
    "/* Auto-generated by the gbs-master build tool. DO NOT EDIT. */",
    `#include "playlist_data.h"`,
    "",
    `const TrackInfo TRACK_LIST[${tracks.length}] = {`,
    entries.join(",\n"),
    "};",
    "",
  ];

  return lines.join("\n");
}

// ── Utilities ─────────────────────────────────────────────────────────────────

function hex16(n: number): string {
  return n.toString(16).toUpperCase().padStart(4, "0");
}

function hex8(n: number): string {
  return n.toString(16).toUpperCase().padStart(2, "0");
}

function truncate(s: string, max: number): string {
  return s.length > max ? s.slice(0, max) : s;
}

/** Escape a string for safe embedding in a C string literal. */
function escapeC(s: string): string {
  return s
    .replace(/\\/g, "\\\\")
    .replace(/"/g, '\\"')
    .replace(/\n/g, "\\n")
    .replace(/\r/g, "");
}

/**
 * Replace non-printable-ASCII characters with "?".
 *
 * Our font only covers ASCII 0x20–0x7E. Bytes outside that range
 * (e.g. 0xE9 for "é" in Latin-1) would render as garbage tiles on screen.
 */
function toAscii(s: string): string {
  return s.replace(/[^\x20-\x7e]/g, "?");
}

// ── Trampoline ASM generation ────────────────────────────────────────────────

/**
 * Generate `trampolines.s` — a small ASM file that provides the `_entry`
 * symbol called by startup.s.
 *
 * In non-banked mode: `_entry` simply jumps to `_main`.
 * In banked mode:     `_entry` switches to the code bank first, and
 *                     additional trampolines wrap GBS INIT/PLAY calls
 *                     and provide a generic banked-copy routine.
 */
export async function generateTrampolines(
  outputDir: string,
  bankedCode: BankedCodeOptions,
  gbsInitAddr: number,
  gbsPlayAddr: number,
): Promise<string> {
  const path = join(outputDir, "trampolines.s");
  const content = bankedCode.enabled
    ? buildBankedTrampolines(bankedCode.codeBankNum, gbsInitAddr, gbsPlayAddr)
    : buildSimpleTrampolines();
  await writeFile(path, content, "utf8");
  return path;
}

function buildSimpleTrampolines(): string {
  return [
    "; Auto-generated by the gbs-master build tool. DO NOT EDIT.",
    "; Non-banked mode: _entry simply jumps to _main.",
    "",
    ".area _TRAMPOLINE (ABS)",
    ".globl _main",
    "",
    ".org 0x0200",
    "_entry::",
    "    jp   _main",
    "",
  ].join("\n");
}

function buildBankedTrampolines(
  codeBankNum: number,
  gbsInitAddr: number,
  gbsPlayAddr: number,
): string {
  const initHex = "0x" + hex16(gbsInitAddr);
  const playHex = "0x" + hex16(gbsPlayAddr);

  return [
    "; Auto-generated by the gbs-master build tool. DO NOT EDIT.",
    "; Banked-code mode: player _CODE lives in a dedicated ROM bank.",
    "; These trampolines live in bank 0 (always visible) and handle",
    "; bank switching around GBS calls and resource bank data copies.",
    "",
    ".area _TRAMPOLINE (ABS)",
    ".globl _main",
    ".globl _bcopy_params",
    "",
    ".org 0x0200",
    "",
    "; ── Entry: switch to code bank and call main (never returns) ─────────",
    "_entry::",
    `    ld   a, #${codeBankNum}`,
    "    ld   (0x2000), a",
    "    call _main",
    "_entry_halt:",
    "    halt",
    "    jr   _entry_halt",
    "",
    "; ── GBS INIT trampoline ──────────────────────────────────────────────",
    "; Called from code bank with A = 0-based track index, SP = GBS stack.",
    "; Caller has already saved our SP to HRAM.",
    "_gbs_init_trampoline::",
    `    call ${initHex}`,
    `    ld   a, #${codeBankNum}`,
    "    ld   (0x2000), a",
    "    ret",
    "",
    "; ── GBS PLAY trampoline ──────────────────────────────────────────────",
    "; Called from code bank with SP = GBS stack.",
    "; Caller has already saved our SP and player_current_track to HRAM.",
    "_gbs_play_trampoline::",
    `    call ${playHex}`,
    `    ld   a, #${codeBankNum}`,
    "    ld   (0x2000), a",
    "    ret",
    "",
    "; ── Generic banked copy ──────────────────────────────────────────────",
    "; Reads BankedCopyParams from WRAM (_bcopy_params):",
    ";   +0: bank (uint8)   +1: src (uint16 LE)   +3: dst (uint16 LE)   +5: len (uint16 LE)",
    "; Switches to the source bank, copies len bytes from src to dst,",
    "; then restores the code bank and returns.",
    "_banked_copy::",
    "    ; Switch to source bank",
    "    ld   a, (_bcopy_params + 0)",
    "    ld   (0x2000), a",
    "    ; Load src into HL",
    "    ld   a, (_bcopy_params + 1)",
    "    ld   l, a",
    "    ld   a, (_bcopy_params + 2)",
    "    ld   h, a",
    "    ; Load dst into DE",
    "    ld   a, (_bcopy_params + 3)",
    "    ld   e, a",
    "    ld   a, (_bcopy_params + 4)",
    "    ld   d, a",
    "    ; Load len into BC",
    "    ld   a, (_bcopy_params + 5)",
    "    ld   c, a",
    "    ld   a, (_bcopy_params + 6)",
    "    ld   b, a",
    "    ; Check for zero length",
    "    ld   a, b",
    "    or   c",
    "    jr   z, _bc_done",
    "_bc_loop:",
    "    ld   a, (hl+)",
    "    ld   (de), a",
    "    inc  de",
    "    dec  bc",
    "    ld   a, b",
    "    or   c",
    "    jr   nz, _bc_loop",
    "_bc_done:",
    `    ld   a, #${codeBankNum}`,
    "    ld   (0x2000), a",
    "    ret",
    "",
    "; ── BankedCopyParams in WRAM ─────────────────────────────────────────",
    ".area _DATA",
    "_bcopy_params::",
    "    .ds 7",
    "",
  ].join("\n");
}

