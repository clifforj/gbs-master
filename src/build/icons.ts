/**
 * Icon asset converter: reads 8x8 PNG files from rom/assets/icons/ and generates
 * GB 2bpp tile data as C source files for inclusion in the ROM build.
 *
 * Icon PNGs use the same GBStudio Classic palette as the fonts:
 *   #E0F8CF → shade 0 (white / transparent for sprites)
 *   #87C06A → shade 1 (light gray)
 *   #2E6850 → shade 2 (dark gray)
 *   #071821 → shade 3 (black)
 *   #FF00FF → shade 0 (magenta = transparent)
 *
 * Four icon slots are supported:
 *   cursor.png  — sprite shown next to the selected track in the list
 *   playing.png — sprite shown next to the currently playing track
 *   album.png   — icon before the album title in the window overlay
 *   track.png   — icon before the now-playing track name in the window overlay
 *
 * If a PNG file is missing, a blank (all-transparent) tile is generated.
 *
 * Output: icon_data.h + icon_data.c in rom/src/generated/
 * Icon tiles are loaded into VRAM tiles 230-233 during ui_init().
 */

import { readFile, writeFile, mkdir, access } from "fs/promises";
import { join } from "path";
import { parsePng } from "./png.js";
import { shadesToGb2bpp } from "./tiles.js";

/** The four icon slots, in order. */
const ICON_NAMES = ["cursor", "playing", "album", "track"] as const;
type IconName = (typeof ICON_NAMES)[number];

/** VRAM tile indices for the icons (tiles 230-233 are free). */
const ICON_TILE_BASE = 230;

/** Bytes per GB 2bpp tile. */
const TILE_BYTES = 16;

/**
 * Read icon PNGs from `assetDir/icons/` and generate icon_data.h + icon_data.c
 * in `outputDir`.
 */
export interface IconResult {
  /** Raw GB 2bpp tile data for all icons (64 bytes). */
  tileData: Uint8Array;
}

export async function generateIcons(
  assetDir: string,
  outputDir: string
): Promise<IconResult> {
  await mkdir(outputDir, { recursive: true });

  const iconDir = join(assetDir, "icons");
  const allTileData = new Uint8Array(ICON_NAMES.length * TILE_BYTES);

  for (let i = 0; i < ICON_NAMES.length; i++) {
    const name = ICON_NAMES[i];
    const pngPath = join(iconDir, `${name}.png`);
    let tileBytes: Uint8Array;

    if (await fileExists(pngPath)) {
      const pngBuf = await readFile(pngPath);
      tileBytes = extractIconTile(pngBuf, `${name}.png`);
    } else {
      // Missing icon → blank (all-transparent) tile
      tileBytes = new Uint8Array(TILE_BYTES);
    }

    allTileData.set(tileBytes, i * TILE_BYTES);
  }

  await writeFile(join(outputDir, "icon_data.h"), buildIconHeader(), "utf8");
  await writeFile(
    join(outputDir, "icon_data.c"),
    buildIconSource(allTileData),
    "utf8"
  );

  return { tileData: allTileData };
}

/**
 * Extract a single 8x8 tile from an icon PNG.
 * The PNG must be exactly 8x8 pixels.
 */
function extractIconTile(pngBuf: Buffer, filename: string): Uint8Array {
  const png = parsePng(pngBuf, filename);

  if (png.width !== 8 || png.height !== 8) {
    throw new Error(
      `${filename}: icon must be 8x8 pixels, got ${png.width}x${png.height}`
    );
  }

  return shadesToGb2bpp(png.shades, 8, 0, 0);
}

async function fileExists(path: string): Promise<boolean> {
  try {
    await access(path);
    return true;
  } catch {
    return false;
  }
}

// ── C code generators ────────────────────────────────────────────────────────

function buildIconHeader(): string {
  const lines: string[] = [
    "/* Auto-generated by the gbs-master build tool. DO NOT EDIT. */",
    "#ifndef ICON_DATA_H",
    "#define ICON_DATA_H",
    "",
    "#include <stdint.h>",
    "",
    "/* VRAM tile indices for each icon (tiles 230-233). */",
  ];

  for (let i = 0; i < ICON_NAMES.length; i++) {
    const name = ICON_NAMES[i].toUpperCase();
    lines.push(`#define ICON_${name}_TILE  ${ICON_TILE_BASE + i}`);
  }

  lines.push(
    "",
    `#define ICON_TILE_COUNT  ${ICON_NAMES.length}`,
    `#define ICON_TILE_BASE   ${ICON_TILE_BASE}`,
    "",
    `/* ${ICON_NAMES.length} tiles x 16 bytes = ${ICON_NAMES.length * TILE_BYTES} bytes of GB 2bpp tile data. */`,
    `extern const uint8_t ICON_TILES[${ICON_NAMES.length * TILE_BYTES}U];`,
    "",
    "/* VRAM address where icon tiles start: 0x8000 + ICON_TILE_BASE * 16. */",
    `#define ICON_VRAM_BASE  ((volatile uint8_t*)0x${(0x8000 + ICON_TILE_BASE * 16).toString(16).toUpperCase()}u)`,
    "",
    "#endif /* ICON_DATA_H */",
    ""
  );

  return lines.join("\n");
}

function buildIconSource(tileData: Uint8Array): string {
  const tileLines: string[] = [];
  for (let i = 0; i < ICON_NAMES.length; i++) {
    const chunk = Array.from(tileData.slice(i * TILE_BYTES, (i + 1) * TILE_BYTES))
      .map((b) => `0x${b.toString(16).padStart(2, "0").toUpperCase()}U`)
      .join(", ");
    tileLines.push(`    /* ${ICON_NAMES[i]} */ ${chunk}`);
  }

  return [
    "/* Auto-generated by the gbs-master build tool. DO NOT EDIT. */",
    `#include "icon_data.h"`,
    "",
    `/* Icon tiles: ${ICON_NAMES.join(", ")} (${ICON_NAMES.length} x 16 bytes, GB 2bpp). */`,
    `const uint8_t ICON_TILES[${tileData.length}U] = {`,
    tileLines.join(",\n"),
    "};",
    "",
  ].join("\n");
}
