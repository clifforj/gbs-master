/**
 * Album cover converter: reads a 16x16 PNG and generates 4 GB 2bpp tiles
 * (2x2 tile grid) as C source files for inclusion in the ROM build.
 *
 * Uses the same GBStudio Classic palette as the icons:
 *   #E0F8CF → shade 0 (white / transparent)
 *   #87C06A → shade 1 (light gray)
 *   #2E6850 → shade 2 (dark gray)
 *   #071821 → shade 3 (black)
 *   #FF00FF → shade 0 (magenta = transparent)
 *
 * If no cover PNG is provided, the default disc at rom/assets/cover.png is used.
 *
 * The 4 tiles are arranged as:
 *   [TL][TR]   (tiles 234-235)
 *   [BL][BR]   (tiles 236-237)
 *
 * Output: cover_data.h + cover_data.c in rom/src/generated/
 * Cover tiles are loaded into VRAM tiles 234-237 during ui_init().
 */

import { readFile, writeFile, mkdir } from "fs/promises";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import { parsePng } from "./png.js";
import { shadesToGb2bpp } from "./tiles.js";

const __dirname = dirname(fileURLToPath(import.meta.url));

/** Path to the default cover PNG (two levels up from dist/build/). */
const DEFAULT_COVER_PATH = join(__dirname, "../../rom/assets/cover.png");

/** VRAM tile indices for the 2x2 cover (right after icon tiles 230-233). */
const COVER_TILE_BASE = 234;

/** Bytes per GB 2bpp tile. */
const TILE_BYTES = 16;

/** Total tiles for a 16x16 image (2x2). */
const COVER_TILES = 4;

/**
 * Convert a 16x16 array of shade values to 4 GB 2bpp tiles.
 * Tile order: TL (0,0)-(7,7), TR (8,0)-(15,7), BL (0,8)-(7,15), BR (8,8)-(15,15).
 */
function shadesToTiles(shades: Uint8Array): Uint8Array {
  const out = new Uint8Array(COVER_TILES * TILE_BYTES);

  const tiles: [number, number, number][] = [
    [0, 0, 0],   // TL
    [1, 8, 0],   // TR
    [2, 0, 8],   // BL
    [3, 8, 8],   // BR
  ];

  for (const [ti, sx, sy] of tiles) {
    const tile = shadesToGb2bpp(shades, 16, sx, sy);
    out.set(tile, ti * TILE_BYTES);
  }

  return out;
}

/**
 * Extract cover tiles from a 16x16 PNG buffer.
 */
function extractCoverTiles(pngBuf: Buffer, filename: string): Uint8Array {
  const png = parsePng(pngBuf, filename);

  if (png.width !== 16 || png.height !== 16) {
    throw new Error(
      `${filename}: cover must be 16x16 pixels, got ${png.width}x${png.height}`
    );
  }

  return shadesToTiles(png.shades);
}

/**
 * Generate cover_data.h + cover_data.c in `outputDir`.
 * If `coverPath` is provided, reads the PNG from that path.
 * Otherwise uses the default cover at rom/assets/cover.png.
 */
export interface CoverResult {
  /** Raw GB 2bpp tile data for the 2x2 cover (64 bytes). */
  tileData: Uint8Array;
}

export async function generateCover(
  outputDir: string,
  coverPath?: string
): Promise<CoverResult> {
  await mkdir(outputDir, { recursive: true });

  const pngPath = coverPath ?? DEFAULT_COVER_PATH;
  const pngBuf = await readFile(pngPath);
  const tileData = extractCoverTiles(pngBuf, pngPath);

  await writeFile(join(outputDir, "cover_data.h"), buildCoverHeader(), "utf8");
  await writeFile(
    join(outputDir, "cover_data.c"),
    buildCoverSource(tileData),
    "utf8"
  );

  return { tileData };
}

// ── C code generators ────────────────────────────────────────────────────────

function buildCoverHeader(): string {
  const vramAddr = 0x8000 + COVER_TILE_BASE * 16;
  return [
    "/* Auto-generated by the gbs-master build tool. DO NOT EDIT. */",
    "#ifndef COVER_DATA_H",
    "#define COVER_DATA_H",
    "",
    "#include <stdint.h>",
    "",
    "/* VRAM tile indices for the 2x2 album cover (tiles 234-237). */",
    `#define COVER_TILE_BASE  ${COVER_TILE_BASE}`,
    `#define COVER_TILE_TL    ${COVER_TILE_BASE}`,
    `#define COVER_TILE_TR    ${COVER_TILE_BASE + 1}`,
    `#define COVER_TILE_BL    ${COVER_TILE_BASE + 2}`,
    `#define COVER_TILE_BR    ${COVER_TILE_BASE + 3}`,
    `#define COVER_TILE_COUNT 4`,
    "",
    `/* 4 tiles x 16 bytes = 64 bytes of GB 2bpp tile data. */`,
    `extern const uint8_t COVER_TILES[64U];`,
    "",
    `/* VRAM address where cover tiles start: 0x8000 + ${COVER_TILE_BASE} * 16. */`,
    `#define COVER_VRAM_BASE  ((volatile uint8_t*)0x${vramAddr.toString(16).toUpperCase()}u)`,
    "",
    "#endif /* COVER_DATA_H */",
    "",
  ].join("\n");
}

function buildCoverSource(tileData: Uint8Array): string {
  const tileNames = ["TL", "TR", "BL", "BR"];
  const tileLines: string[] = [];
  for (let i = 0; i < COVER_TILES; i++) {
    const chunk = Array.from(tileData.slice(i * TILE_BYTES, (i + 1) * TILE_BYTES))
      .map((b) => `0x${b.toString(16).padStart(2, "0").toUpperCase()}U`)
      .join(", ");
    tileLines.push(`    /* ${tileNames[i]} */ ${chunk}`);
  }

  return [
    "/* Auto-generated by the gbs-master build tool. DO NOT EDIT. */",
    `#include "cover_data.h"`,
    "",
    `/* Cover tiles: TL, TR, BL, BR (4 x 16 bytes, GB 2bpp). */`,
    `const uint8_t COVER_TILES[${tileData.length}U] = {`,
    tileLines.join(",\n"),
    "};",
    "",
  ].join("\n");
}
